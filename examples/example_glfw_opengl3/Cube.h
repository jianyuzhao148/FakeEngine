// TODO:未来Cube将再次被抽象
class Cube :public GameObject{
private:
    GLuint VAO, VBO;
    Shader shader;
    glm::mat4 defalutModelMat;
    glm::mat4 defalutProjectionMat;
    glm::mat4 defalutViewMat;

public:
    Cube() :GameObject("Cube") {
        defalutModelMat = { 1.0f };
        defalutProjectionMat = glm::perspective(glm::radians(45.0f), (float)1280 / 720, 0.1f, 100.0f);
        defalutViewMat= { 1.0f };
        defalutViewMat = glm::translate(defalutViewMat, glm::vec3(0.0f, 0.0f, -3.0f));
        shader = Shader("vetex.glsl", "fragment.glsl");

        glGenVertexArrays(1, &VAO);
        glBindVertexArray(VAO);

        glGenBuffers(1,&VBO);
        glBindBuffer(GL_ARRAY_BUFFER, VBO);
        glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), cubeVertices, GL_STATIC_DRAW);

        glBindBuffer(GL_ARRAY_BUFFER, VBO);
        glEnableVertexAttribArray(0);
        glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
        glEnableVertexAttribArray(1);
        glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3*sizeof(float)));

        glBindVertexArray(0);
    }

    void Draw(glm::mat4 lookat) {
        shader.use();
        shader.setMat4("mvp", defalutModelMat * defalutProjectionMat * defalutViewMat*lookat);

        glBindVertexArray(VAO);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);
    }
};
