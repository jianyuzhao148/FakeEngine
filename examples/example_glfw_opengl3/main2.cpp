        glClearColor(clear_color.x * clear_color.w, clear_color.y * clear_color.w, clear_color.z * clear_color.w, clear_color.w);
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
        //ImGui::End();

        // Rendering
        ImGui::Render();

        glViewport(viewPos.x,viewPos.y,viewSize.x,viewSize.y);
        ImGui_ImplOpenGL3_RenderDrawData(ImGui::GetDrawData());

        shader.use();

        double timeValue = glfwGetTime();
        glm::mat4 view(1.0f);
        view = glm::translate(view, glm::vec3(0.0f, 0.0f, -3.0f));
        glm::mat4 projection;
        projection = glm::perspective(glm::radians(45.0f), 1280 / 720.0f, 0.1f, 100.0f);
        float greenValue = static_cast<float>(sin(timeValue) / 2.0 + 0.5);
        shader.setVec3("shareColor", 0.0f, greenValue, 0.0f);
        shader.setInt("texture1", 0);
        shader.setInt("texture2", 1);
        shader.setMat4("view",view);
        shader.setMat4("projection",projection);

        glBindVertexArray(VAO);
        glm::mat4 model(1.0f);
        model = glm::rotate(model, (float)glfwGetTime() * glm::radians(90.0f), glm::vec3(0.5f, 1.0f, 0.0f));
        shader.setMat4("model",model);

        glDrawArrays(GL_TRIANGLES, 0, 36);
        //glDrawArrays(GL_TRIANGLES, 0, 3);
        glBindVertexArray(0);

